            int dialogButton = JOptionPane.YES_NO_OPTION;  
            int response;
            response = JOptionPane.showConfirmDialog (TeleFrame, "Desea salir de la Aplicacion?\nSe perderan todos los productos escogidos","Finalizar programa", dialogButton);
            
            
